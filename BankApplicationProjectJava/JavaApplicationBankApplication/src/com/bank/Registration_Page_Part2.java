    private void REGISTERBUTTONActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_REGISTERBUTTONActionPerformed
        try{
           Database D=new Database();
           D.dbConnect();

            String sql="insert into register2 values(?,?,?,?,?,?)";
            PreparedStatement pstmt=D.dbcon.prepareStatement(sql);
            pstmt.setInt(2,Integer.parseInt(account_no.getText()));
            pstmt.setString(1,account_nominee.getText());
            
            pstmt.setInt(3,Integer.parseInt(get_account_pin.getText()));
            pstmt.setLong(4,Long.parseLong(get_addhar.getText()));
           
            String Acctype;
            Acctype=Account_type.getSelectedItem().toString();
            pstmt.setString(5,Acctype);
            pstmt.setInt(6,Integer.parseInt(balance_getter.getText()));
            
            
            pstmt.executeUpdate();

            JOptionPane.showMessageDialog(null,"Registration Successful \n You Can Now reutrn to Login page");
             this.dispose();
             Customer_login cl=new Customer_login();
             cl.setVisible(true);
        }catch(Exception e)
        {

            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_REGISTERBUTTONActionPerformed

