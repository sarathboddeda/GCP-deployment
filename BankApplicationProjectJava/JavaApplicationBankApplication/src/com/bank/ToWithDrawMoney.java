    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     try{
         String balance=get_account_balance.getText();
         String withdraw=amount_to_withdraw.getText();
         float bal=Float.valueOf(balance);
        float with=Float.valueOf(withdraw);
        
        if(with<bal&&with>=0)
        {
         float sum=Float.parseFloat(balance)-Float.parseFloat(withdraw);
         String total=String.valueOf(sum);
         calculated_amount.setText(total);
         Database D=new Database();
         D.dbConnect();
         String total_for_query=calculated_amount.getText();
         String acc_no=account_number.getText();
        String withdraw_query="UPDATE register2 set Balance='"+total_for_query+"'where acc_number='"+acc_no+"'";
            PreparedStatement pstmt=D.dbcon.prepareStatement(withdraw_query);
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null,"MONEY WITHDRAWN");
        }else
        {
            JOptionPane.showMessageDialog(null,"Amount is Exceeding\n Or It is a Negative Number");
        }
     }catch(Exception e){
         JOptionPane.showMessageDialog(null,e);
     }
    }//GEN-LAST:event_jButton4ActionPerformed

